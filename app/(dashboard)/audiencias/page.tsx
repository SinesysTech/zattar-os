export default function AudienciasPage() {
  const [busca, setBusca] = React.useState('');
  const [pagina, setPagina] = React.useState(0);
  const [limite, setLimite] = React.useState(50);
  const [ordenarPor, setOrdenarPor] = React.useState<
    'data_inicio' | 'numero_processo' | 'polo_ativo_nome' | 'polo_passivo_nome' | null
  >('data_inicio');
  const [ordem, setOrdem] = React.useState<'asc' | 'desc'>('asc');
  const [status, setStatus] = React.useState<'M' | 'F' | 'C' | 'todos'>('M'); // Default: Designada
  const [filtros, setFiltros] = React.useState<AudienciasFilters>({});
  const [visualizacao, setVisualizacao] = React.useState<'tabela' | 'semana' | 'mes' | 'ano'>('tabela');
  const [semanaAtual, setSemanaAtual] = React.useState(new Date());
  const [mesAtual, setMesAtual] = React.useState(new Date());
  const [anoAtual, setAnoAtual] = React.useState(new Date().getFullYear());

  // Debounce da busca
  const buscaDebounced = useDebounce(busca, 500);

  // Parâmetros para buscar audiências
  const params = React.useMemo(
    () => ({
      pagina: pagina + 1, // API usa 1-indexed
      limite,
      busca: buscaDebounced || undefined,
      ordenar_por: ordenarPor || undefined,
      ordem,
      status: status !== 'todos' ? status : undefined, // Filtro de status
      ...filtros, // Spread dos filtros avançados
    }),
    [pagina, limite, buscaDebounced, ordenarPor, ordem, status, filtros]
  );

  const { audiencias: audienciasRaw, paginacao, isLoading, error, refetch } = useAudiencias(params);

  // Buscar usuários uma única vez para compartilhar entre todas as células
  const { usuarios } = useUsuarios({ ativo: true, limite: 100 });

  // Ordenar por data normalizada quando ordenar por data_inicio
  const audiencias = React.useMemo(() => {
    if (!audienciasRaw || ordenarPor !== 'data_inicio') {
      return audienciasRaw;
    }

    // Ordenar por data normalizada (ignorando hora)
    const audienciasOrdenadas = [...audienciasRaw].sort((a, b) => {
      const dataA = normalizarDataParaComparacao(a.data_inicio);
      const dataB = normalizarDataParaComparacao(b.data_inicio);
      return ordem === 'asc' ? dataA - dataB : dataB - dataA;
    });

    return audienciasOrdenadas;
  }, [audienciasRaw, ordenarPor, ordem]);

  const handleSuccess = React.useCallback(() => {
    refetch();
  }, [refetch]);

  const colunas = React.useMemo(
    () => criarColunas(handleSuccess, usuarios),
    [handleSuccess, usuarios]
  );

  const handleSortingChange = React.useCallback(
    (columnId: string | null, direction: 'asc' | 'desc' | null) => {
      if (columnId && direction) {
        setOrdenarPor(columnId as typeof ordenarPor);
        setOrdem(direction);
      } else {
        setOrdenarPor(null);
        setOrdem('asc');
      }
    },
    []
  );

  const handleFiltersChange = React.useCallback((newFilters: AudienciasFilters) => {
    setFiltros(newFilters);
    setPagina(0); // Resetar para primeira página ao aplicar filtros
  }, []);

  const handleFiltersReset = React.useCallback(() => {
    setFiltros({});
    setPagina(0);
  }, []);

  // Funções para navegação de semana
  const navegarSemana = React.useCallback((direcao: 'anterior' | 'proxima') => {
    const novaSemana = new Date(semanaAtual);
    novaSemana.setDate(novaSemana.getDate() + (direcao === 'proxima' ? 7 : -7));
    setSemanaAtual(novaSemana);
  }, [semanaAtual]);

  const voltarSemanaAtual = React.useCallback(() => {
    setSemanaAtual(new Date());
  }, []);

  // Funções para navegação de mês
  const navegarMes = React.useCallback((direcao: 'anterior' | 'proximo') => {
    const novoMes = new Date(mesAtual);
    novoMes.setMonth(novoMes.getMonth() + (direcao === 'proximo' ? 1 : -1));
    setMesAtual(novoMes);
  }, [mesAtual]);

  const voltarMesAtual = React.useCallback(() => {
    setMesAtual(new Date());
  }, []);

  // Funções para navegação de ano
  const navegarAno = React.useCallback((direcao: 'anterior' | 'proximo') => {
    setAnoAtual(direcao === 'proximo' ? anoAtual + 1 : anoAtual - 1);
  }, [anoAtual]);

  const voltarAnoAtual = React.useCallback(() => {
    setAnoAtual(new Date().getFullYear());
  }, []);

  // Calcular início e fim da semana para exibição
  const { inicioSemana, fimSemana } = React.useMemo(() => {
    const date = new Date(semanaAtual);
    date.setHours(0, 0, 0, 0);
    const day = date.getDay();
    const diff = day === 0 ? -6 : 1 - day;
    const inicio = new Date(date);
    inicio.setDate(date.getDate() + diff);

    const fim = new Date(inicio);
    fim.setDate(fim.getDate() + 4);
    fim.setHours(23, 59, 59, 999);

    return { inicioSemana: inicio, fimSemana: fim };
  }, [semanaAtual]);

  const formatarDataCabecalho = (data: Date) => {
    return data.toLocaleDateString('pt-BR', {
      day: '2-digit',
      month: '2-digit',
      year: 'numeric',
    });
  };

  const formatarMesAno = (data: Date) => {
    return data.toLocaleDateString('pt-BR', {
      month: 'long',
      year: 'numeric',
    });
  };

  return (
    <Tabs value={visualizacao} onValueChange={(value) => setVisualizacao(value as typeof visualizacao)}>
      <div className="space-y-4">
        {/* Barra de busca, filtros e tabs de visualização */}
        <div className="flex items-center gap-4">
          <Input
            placeholder="Buscar por número do processo, parte autora ou parte ré..."
            value={busca}
            onChange={(e) => {
              setBusca(e.target.value);
              setPagina(0); // Resetar para primeira página ao buscar
            }}
            className="max-w-sm"
          />
          <AudienciasFiltrosAvancados
            filters={filtros}
            onFiltersChange={handleFiltersChange}
            onReset={handleFiltersReset}
          />
          <Select
            value={status}
            onValueChange={(value) => {
              setStatus(value as 'M' | 'F' | 'C' | 'todos');
              setPagina(0); // Resetar para primeira página ao mudar status
            }}
          >
            <SelectTrigger className="w-[180px]">
              <SelectValue placeholder="Status" />
            </SelectTrigger>
            <SelectContent>
              {STATUS_AUDIENCIA_OPTIONS.map((option) => (
                <SelectItem key={option.value} value={option.value}>
                  {option.label}
                </SelectItem>
              ))}
              <SelectItem value="todos">Todos os Status</SelectItem>
            </SelectContent>
          </Select>

          {/* Tabs de visualização */}
          <TabsList>
            <TabsTrigger value="tabela">Tabela</TabsTrigger>
            <TabsTrigger value="semana">Semana</TabsTrigger>
            <TabsTrigger value="mes">Mês</TabsTrigger>
            <TabsTrigger value="ano">Ano</TabsTrigger>
          </TabsList>

          {/* Controles de navegação de semana (aparecem apenas na visualização de semana) */}
          {visualizacao === 'semana' && (
            <div className="flex items-center gap-2">
              <Button
                variant="outline"
                size="icon"
                onClick={() => navegarSemana('anterior')}
              >
                <ChevronLeft className="h-4 w-4" />
              </Button>
              <div className="text-sm font-medium whitespace-nowrap">
                {formatarDataCabecalho(inicioSemana)} - {formatarDataCabecalho(fimSemana)}
              </div>
              <Button
                variant="outline"
                size="icon"
                onClick={() => navegarSemana('proxima')}
              >
                <ChevronRight className="h-4 w-4" />
              </Button>
            </div>
          )}

          {/* Controles de navegação de mês (aparecem apenas na visualização de mês) */}
          {visualizacao === 'mes' && (
            <div className="flex items-center gap-2">
              <Button
                variant="outline"
                size="icon"
                onClick={() => navegarMes('anterior')}
              >
                <ChevronLeft className="h-4 w-4" />
              </Button>
              <div className="text-sm font-medium whitespace-nowrap capitalize min-w-[160px] text-center">
                {formatarMesAno(mesAtual)}
              </div>
              <Button
                variant="outline"
                size="icon"
                onClick={() => navegarMes('proximo')}
              >
                <ChevronRight className="h-4 w-4" />
              </Button>
            </div>
          )}

          {/* Controles de navegação de ano (aparecem apenas na visualização de ano) */}
          {visualizacao === 'ano' && (
            <div className="flex items-center gap-2">
              <Button
                variant="outline"
                size="icon"
                onClick={() => navegarAno('anterior')}
              >
                <ChevronLeft className="h-4 w-4" />
              </Button>
              <div className="text-sm font-medium whitespace-nowrap min-w-[80px] text-center">
                {anoAtual}
              </div>
              <Button
                variant="outline"
                size="icon"
                onClick={() => navegarAno('proximo')}
              >
                <ChevronRight className="h-4 w-4" />
              </Button>
            </div>
          )}

          {/* Botão atual na extremidade direita */}
          {visualizacao === 'semana' && (
            <Button
              variant="outline"
              onClick={voltarSemanaAtual}
              className="ml-auto"
            >
              Semana Atual
            </Button>
          )}
          {visualizacao === 'mes' && (
            <Button
              variant="outline"
              onClick={voltarMesAtual}
              className="ml-auto"
            >
              Mês Atual
            </Button>
          )}
          {visualizacao === 'ano' && (
            <Button
              variant="outline"
              onClick={voltarAnoAtual}
              className="ml-auto"
            >
              Ano Atual
            </Button>
          )}
        </div>

        <TabsContent value="tabela" className="mt-0">
          {/* Tabela */}
          <DataTable
            data={audiencias}
            columns={colunas}
            pagination={
              paginacao
                ? {
                    pageIndex: paginacao.pagina - 1, // Converter para 0-indexed
                    pageSize: paginacao.limite,
                    total: paginacao.total,
                    totalPages: paginacao.totalPaginas,
                    onPageChange: setPagina,
                    onPageSizeChange: setLimite,
                  }
                : undefined
            }
            sorting={{
              columnId: ordenarPor,
              direction: ordem,
              onSortingChange: handleSortingChange,
            }}
            isLoading={isLoading}
            error={error}
            emptyMessage="Nenhuma audiência encontrada."
          />
        </TabsContent>

        <TabsContent value="semana" className="mt-0">
          <AudienciasVisualizacaoSemana
            audiencias={audiencias}
            isLoading={isLoading}
            semanaAtual={semanaAtual}
            usuarios={usuarios}
            onRefresh={refetch}
          />
        </TabsContent>

        <TabsContent value="mes" className="mt-0">
          <AudienciasVisualizacaoMes
            audiencias={audiencias}
            isLoading={isLoading}
            mesAtual={mesAtual}
          />
        </TabsContent>

        <TabsContent value="ano" className="mt-0">
          <AudienciasVisualizacaoAno
            audiencias={audiencias}
            isLoading={isLoading}
            anoAtual={anoAtual}
          />
        </TabsContent>
      </div>
    </Tabs>
  );
}

