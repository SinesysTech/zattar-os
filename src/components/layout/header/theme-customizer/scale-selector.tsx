"use client";

import { Label } from "@/components/ui/label";
import { useThemeConfig } from "@/components/layout/theme/active-theme";
import { ToggleGroup, ToggleGroupItem } from "@/components/ui/toggle-group";
import { BanIcon } from "lucide-react";

interface ThemeScaleSelectorProps {
  label?: string;
}

export function ThemeScaleSelector({ 
  label = "Scale"
}: ThemeScaleSelectorProps = {}) {
  const { theme, setTheme } = useThemeConfig();

  return (
    <div className="flex flex-col gap-4">
      <Label htmlFor="roundedCorner">{label}</Label>
      <div>
        <ToggleGroup
          value={theme.scale}
          type="single"
          onValueChange={(value) => setTheme({ ...theme, scale: value as typeof theme.scale })}
          className="*:border-input w-full gap-3 *:rounded-md *:border">
          <ToggleGroupItem variant="outline" value="none">
            <BanIcon />
          </ToggleGroupItem>
          <ToggleGroupItem
            variant="outline"
            value="sm"
            className="text-xs data-[variant=outline]:border-l">
            XS
          </ToggleGroupItem>
          <ToggleGroupItem
            variant="outline"
            value="lg"
            className="text-xs data-[variant=outline]:border-l">
            LG
          </ToggleGroupItem>
        </ToggleGroup>
      </div>
    </div>
  );
}
