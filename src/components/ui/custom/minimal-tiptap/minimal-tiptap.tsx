import * as React from "react";
import "./styles/index.css";

import type { Content, Editor } from "@tiptap/react";
import type { UseMinimalTiptapEditorProps } from "./hooks/use-minimal-tiptap";
import { EditorContent } from "@tiptap/react";
import { Separator } from "@/components/ui/separator";
import { cn } from "@/lib/utils";
import { SectionTwo } from "./components/section/two";
import { SectionFour } from "./components/section/four";
import { SectionFive } from "./components/section/five";
import { useMinimalTiptapEditor } from "./hooks/use-minimal-tiptap";
import { MeasuredContainer } from "./components/measured-container";

export interface MinimalTiptapProps extends Omit<UseMinimalTiptapEditorProps, "onUpdate"> {
  value?: Content;
  onChange?: (value: Content) => void;
  className?: string;
  editorContentClassName?: string;
  toolbarRight?: React.ReactNode;
}

const Toolbar = ({ editor, toolbarRight }: { editor: Editor; toolbarRight?: React.ReactNode }) => (
  <div className="shrink-0 border-b border-border p-2">
    <div className="flex items-center gap-2">
      {/* À esquerda: toolbar principal (scrollável quando necessário) */}
      <div className="min-w-0 flex-1 overflow-x-auto">
        <div className="flex w-max items-center gap-px">
          {/* Formatação */}
          <SectionTwo
            editor={editor}
            activeActions={["bold", "italic", "underline", "strikethrough", "code", "clearFormatting"]}
            mainActionCount={3}
          />

          <Separator orientation="vertical" className="mx-2 h-7" />

          {/* Listas */}
          <SectionFour editor={editor} activeActions={["orderedList", "bulletList"]} mainActionCount={0} />

          <Separator orientation="vertical" className="mx-2 h-7" />

          {/* Inserções (link, imagem, etc.) */}
          <SectionFive editor={editor} mainActionCount={0} />
        </div>
      </div>

      {/* À direita: ações externas (fixas) */}
      {toolbarRight ? (
        <div className="flex shrink-0 items-center gap-2">
          <Separator orientation="vertical" className="h-7" />
          <div className="flex items-center gap-1">{toolbarRight}</div>
        </div>
      ) : null}
    </div>
  </div>
);

export const MinimalTiptapEditor = React.forwardRef<HTMLDivElement, MinimalTiptapProps>(
  ({ value, onChange, className, editorContentClassName, toolbarRight, ...props }, ref) => {
    const editor = useMinimalTiptapEditor({
      value,
      onUpdate: onChange,
      ...props
    });

    if (!editor) {
      return null;
    }

    return (
      <MeasuredContainer
        as="div"
        name="editor"
        ref={ref}
        className={cn(
          "flex h-auto min-h-72 w-full flex-col rounded-md border border-input shadow-xs focus-within:border-primary",
          className
        )}>
        <Toolbar editor={editor} toolbarRight={toolbarRight} />
        <EditorContent
          editor={editor}
          className={cn("minimal-tiptap-editor", editorContentClassName)}
        />
      </MeasuredContainer>
    );
  }
);

MinimalTiptapEditor.displayName = "MinimalTiptapEditor";

export default MinimalTiptapEditor;
