'use client';

/**
 * ExpedientesDayList - Lista de expedientes do dia selecionado
 *
 * Componente para exibição no painel direito do layout master-detail.
 * Mostra todos os expedientes de um dia específico em cards compactos.
 */

import * as React from 'react';
import { format, isSameDay, parseISO } from 'date-fns';
import { ptBR } from 'date-fns/locale';
import { Calendar, Plus } from 'lucide-react';

import { cn } from '@/lib/utils';
import { Button } from '@/components/ui/button';
import { ScrollArea } from '@/components/ui/scroll-area';
import { AppBadge } from '@/components/ui/app-badge';

import type { Expediente } from '../domain';

// =============================================================================
// TIPOS
// =============================================================================

interface ExpedientesDayListProps {
  /** Data selecionada para exibir expedientes */
  selectedDate: Date;
  /** Todos os expedientes (serão filtrados pelo dia) */
  expedientes: Expediente[];
  /** Callback para adicionar novo expediente */
  onAddExpediente?: () => void;
  /** Classes CSS adicionais */
  className?: string;
}

// =============================================================================
// COMPONENTE PRINCIPAL
// =============================================================================

export function ExpedientesDayList({
  selectedDate,
  expedientes,
  onAddExpediente,
  className,
}: ExpedientesDayListProps) {
  // Filtrar expedientes do dia selecionado
  const expedientesDoDia = React.useMemo(() => {
    return expedientes
      .filter((exp) => {
        // Expedientes sem data não aparecem
        if (!exp.dataPrazoLegalParte) return false;

        return isSameDay(parseISO(exp.dataPrazoLegalParte), selectedDate);
      })
      .sort((a, b) => {
        // Vencidos (prazoVencido === true AND baixadoEm is null) primeiro
        const aVencidoPendente = a.prazoVencido && !a.baixadoEm;
        const bVencidoPendente = b.prazoVencido && !b.baixadoEm;

        if (aVencidoPendente && !bVencidoPendente) return -1;
        if (!aVencidoPendente && bVencidoPendente) return 1;

        // Depois ordenar por data
        const dateA = a.dataPrazoLegalParte ? parseISO(a.dataPrazoLegalParte).getTime() : 0;
        const dateB = b.dataPrazoLegalParte ? parseISO(b.dataPrazoLegalParte).getTime() : 0;
        return dateA - dateB;
      });
  }, [expedientes, selectedDate]);

  // Formatar header da data (ex: "Quarta, 12 de Fevereiro")
  const dateHeader = React.useMemo(() => {
    const formatted = format(selectedDate, "EEEE, d 'de' MMMM", { locale: ptBR });
    return formatted.charAt(0).toUpperCase() + formatted.slice(1);
  }, [selectedDate]);

  // Determinar status do expediente
  const getStatus = React.useCallback((expediente: Expediente) => {
    if (expediente.baixadoEm) {
      return { label: 'Baixado', variant: 'success' as const };
    }
    if (expediente.prazoVencido) {
      return { label: 'Vencido', variant: 'destructive' as const };
    }
    return { label: 'Pendente', variant: 'default' as const };
  }, []);

  return (
    <div className={cn('flex flex-col h-full', className)}>
      {/* Header */}
      <div className="flex items-center justify-between px-4 py-3 border-b bg-muted/30">
        <div className="flex items-center gap-2">
          <Calendar className="h-4 w-4 text-muted-foreground" />
          <span className="font-medium">{dateHeader}</span>
        </div>
        {onAddExpediente && (
          <Button
            variant="ghost"
            size="sm"
            className="h-7 px-2 text-xs"
            onClick={onAddExpediente}
          >
            <Plus className="h-3.5 w-3.5 mr-1" />
            Novo
          </Button>
        )}
      </div>

      {/* Lista de expedientes */}
      {expedientesDoDia.length > 0 ? (
        <>
          <ScrollArea className="flex-1">
            <div className="p-3 space-y-2">
              {expedientesDoDia.map((expediente) => {
                const status = getStatus(expediente);

                return (
                  <div
                    key={expediente.id}
                    className="p-3 rounded-lg border bg-card hover:bg-accent/50 transition-colors space-y-1.5"
                  >
                    {/* Primeira linha: número do processo + badge status */}
                    <div className="flex items-start justify-between gap-2">
                      <span className="text-sm font-medium truncate">
                        {expediente.numeroProcesso}
                      </span>
                      <AppBadge variant={status.variant} className="shrink-0 text-xs">
                        {status.label}
                      </AppBadge>
                    </div>

                    {/* Segunda linha: tipo expediente + descrição arquivos */}
                    <div className="text-xs text-muted-foreground">
                      <span className="font-medium">
                        {(expediente as any).tipoExpediente?.tipoExpediente || 'Sem tipo'}
                      </span>
                      {expediente.descricaoArquivos && (
                        <span className="ml-1.5">
                          • {expediente.descricaoArquivos.length > 50
                              ? `${expediente.descricaoArquivos.slice(0, 50)}...`
                              : expediente.descricaoArquivos}
                        </span>
                      )}
                    </div>

                    {/* Terceira linha: partes */}
                    <div className="text-xs text-muted-foreground/80 truncate">
                      {expediente.nomeParteAutoraOrigem || expediente.nomeParteAutora || '-'}
                      {' vs '}
                      {expediente.nomeParteReOrigem || expediente.nomeParteRe || '-'}
                    </div>
                  </div>
                );
              })}
            </div>
          </ScrollArea>

          {/* Footer com contagem */}
          <div className="px-4 py-2 border-t bg-muted/20">
            <AppBadge variant="secondary" className="text-xs">
              {expedientesDoDia.length} {expedientesDoDia.length === 1 ? 'expediente' : 'expedientes'}
            </AppBadge>
          </div>
        </>
      ) : (
        /* Empty state */
        <div className="flex-1 flex flex-col items-center justify-center text-center p-6">
          <Calendar className="h-12 w-12 text-muted-foreground/30 mb-3" />
          <p className="text-sm text-muted-foreground">
            Nenhum expediente neste dia
          </p>
          {onAddExpediente && (
            <Button
              variant="outline"
              size="sm"
              className="mt-3"
              onClick={onAddExpediente}
            >
              <Plus className="h-4 w-4 mr-1" />
              Novo Expediente
            </Button>
          )}
        </div>
      )}
    </div>
  );
}
