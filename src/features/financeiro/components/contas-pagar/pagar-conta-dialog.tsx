'use client';

/**
 * Dialog para Pagamento de Conta a Pagar
 */

import * as React from 'react';
import { toast } from 'sonner';
import { format } from 'date-fns';
import { ptBR } from 'date-fns/locale';
import { CalendarIcon, CreditCard, Building2, AlertCircle, Upload, X, FileText, Loader2 } from 'lucide-react';
import { createClient } from '@/lib/supabase/client';
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogFooter,
  DialogHeader,
  DialogTitle,
} from '@/components/ui/dialog';
import { Button } from '@/components/ui/button';
import { Label } from '@/components/ui/label';
import { Textarea } from '@/components/ui/textarea';
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select';
import { Calendar } from '@/components/ui/calendar';
import { Popover, PopoverContent, PopoverTrigger } from '@/components/ui/popover';
import { Alert, AlertDescription } from '@/components/ui/alert';
import { Separator } from '@/components/ui/separator';
import { cn } from '@/lib/utils';
import { actionAtualizarLancamento } from '../../actions/lancamentos';
import type {
  ContaPagarComDetalhes,
  FormaPagamentoContaPagar,
  AnexoLancamento,
  Lancamento,
} from '../../types/lancamentos';

interface PagarContaDialogProps {
  open: boolean;
  onOpenChange: (open: boolean) => void;
  conta: ContaPagarComDetalhes | null;
  contasBancarias: Array<{ id: number; nome: string; banco?: string | null }>;
  onSuccess: () => void;
}

const FORMAS_PAGAMENTO: Array<{ value: FormaPagamentoContaPagar; label: string }> = [
  { value: 'pix', label: 'PIX' },
  { value: 'transferencia_bancaria', label: 'Transferência Bancária' },
  { value: 'ted', label: 'TED' },
  { value: 'boleto', label: 'Boleto' },
  { value: 'dinheiro', label: 'Dinheiro' },
  { value: 'cheque', label: 'Cheque' },
  { value: 'cartao_debito', label: 'Cartão de Débito' },
  { value: 'cartao_credito', label: 'Cartão de Crédito' },
  { value: 'deposito_judicial', label: 'Depósito Judicial' },
];

/**
 * Formata valor em reais
 */
const formatarValor = (valor: number): string => {
  return new Intl.NumberFormat('pt-BR', {
    style: 'currency',
    currency: 'BRL',
  }).format(valor);
};

// Tipos de arquivo permitidos para comprovante
const COMPROVANTE_ALLOWED_TYPES = ['image/jpeg', 'image/png', 'image/webp', 'application/pdf'];
const COMPROVANTE_MAX_SIZE = 10 * 1024 * 1024; // 10MB

export function PagarContaDialog({
  open,
  onOpenChange,
  conta,
  contasBancarias,
  onSuccess,
}: PagarContaDialogProps) {
  const [formaPagamento, setFormaPagamento] = React.useState<FormaPagamentoContaPagar | ''>('');
  const [contaBancariaId, setContaBancariaId] = React.useState<string>('');
  const [dataEfetivacao, setDataEfetivacao] = React.useState<Date | undefined>(new Date());
  const [observacoes, setObservacoes] = React.useState('');
  const [isSubmitting, setIsSubmitting] = React.useState(false);

  // Estados para comprovante
  const [comprovanteFile, setComprovanteFile] = React.useState<File | null>(null);
  const [comprovanteUploading, setComprovanteUploading] = React.useState(false);
  const [comprovanteError, setComprovanteError] = React.useState<string | null>(null);

  // Reset form quando dialog abre
  React.useEffect(() => {
    if (open) {
      setFormaPagamento('');
      setContaBancariaId('');
      setDataEfetivacao(new Date());
      setObservacoes('');
      setComprovanteFile(null);
      setComprovanteError(null);
    }
  }, [open]);

  /**
   * Valida o arquivo de comprovante
   */
  const validateComprovanteFile = (file: File): string | null => {
    if (!COMPROVANTE_ALLOWED_TYPES.includes(file.type)) {
      return 'Tipo de arquivo não permitido. Use PDF, JPG, PNG ou WEBP.';
    }
    if (file.size > COMPROVANTE_MAX_SIZE) {
      return 'Arquivo muito grande. Tamanho máximo: 10MB.';
    }
    return null;
  };

  /**
   * Faz upload do comprovante e retorna o objeto AnexoLancamento
   */
  const uploadComprovante = async (file: File): Promise<AnexoLancamento | null> => {
    setComprovanteUploading(true);
    setComprovanteError(null);

    try {
      const supabase = createClient();

      // Criar nome único para o arquivo
      const timestamp = Date.now();
      const randomId = Math.random().toString(36).substring(7);
      const extension = file.name.split('.').pop();
      const uniqueName = `comprovantes/${timestamp}-${randomId}.${extension}`;

      // Upload para Supabase Storage
      const { error } = await supabase.storage
        .from('financeiro')
        .upload(uniqueName, file, {
          cacheControl: '3600',
          upsert: false,
        });

      if (error) throw error;

      // Obter URL pública
      const { data: { publicUrl } } = supabase.storage
        .from('financeiro')
        .getPublicUrl(uniqueName);

      return {
        nome: file.name,
        url: publicUrl,
        tipo: file.type,
        tamanho: file.size,
        uploadedAt: new Date().toISOString(),
      };
    } catch (error) {
      console.error('Erro ao fazer upload do comprovante:', error);
      setComprovanteError('Erro ao fazer upload do comprovante');
      return null;
    } finally {
      setComprovanteUploading(false);
    }
  };

  /**
   * Handler para seleção de arquivo
   */
  const handleFileSelect = (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0];
    if (!file) return;

    const validationError = validateComprovanteFile(file);
    if (validationError) {
      setComprovanteError(validationError);
      return;
    }

    setComprovanteFile(file);
    setComprovanteError(null);
    e.target.value = ''; // Reset input
  };

  /**
   * Remove o arquivo selecionado
   */
  const handleRemoveFile = () => {
    setComprovanteFile(null);
    setComprovanteError(null);
  };

  /**
   * Formata tamanho de arquivo
   */
  const formatFileSize = (bytes: number): string => {
    if (bytes === 0) return '0 Bytes';
    const k = 1024;
    const sizes = ['Bytes', 'KB', 'MB'];
    const i = Math.floor(Math.log(bytes) / Math.log(k));
    return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();

    if (!conta || !formaPagamento || !contaBancariaId) {
      toast.error('Preencha todos os campos obrigatórios');
      return;
    }

    setIsSubmitting(true);

    try {
      // Upload do comprovante se houver
      let comprovante: AnexoLancamento | undefined;
      if (comprovanteFile) {
        const uploadedComprovante = await uploadComprovante(comprovanteFile);
        if (uploadedComprovante) {
          comprovante = uploadedComprovante;
        }
        // Se houver erro no upload, não continua
        if (comprovanteError) {
          setIsSubmitting(false);
          return;
        }
      }

      // Preparar payload de atualização
      const payload: Partial<Lancamento> = {
        status: 'confirmado',
        formaPagamento,
        contaBancariaId: parseInt(contaBancariaId, 10),
        dataEfetivacao: dataEfetivacao?.toISOString(),
        observacoes: observacoes.trim() || undefined,
      };

      if (comprovante) {
        // Assume que anexos existing devem ser mantidos ou substituídos?
        // Aqui estamos adicionando ao array existente ou criando novo array
        payload.anexos = [...(conta.anexos || []), comprovante];
      }

      const resultado = await actionAtualizarLancamento(conta.id, payload);

      if (!resultado.sucesso) {
        throw new Error(resultado.erro || 'Erro ao efetuar pagamento');
      }

      toast.success('Pagamento efetuado com sucesso!');
      onOpenChange(false);
      onSuccess();
    } catch (error) {
      const message = error instanceof Error ? error.message : 'Erro ao efetuar pagamento';
      toast.error(message);
    } finally {
      setIsSubmitting(false);
    }
  };

  if (!conta) {
    return null;
  }

  const isVencida = conta.dataVencimento && new Date(conta.dataVencimento) < new Date();

  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="sm:max-w-[500px]">
        <form onSubmit={handleSubmit}>
          <DialogHeader>
            <DialogTitle className="flex items-center gap-2">
              <CreditCard className="h-5 w-5" />
              Efetuar Pagamento
            </DialogTitle>
            <DialogDescription>
              Confirme os dados do pagamento para esta conta a pagar.
            </DialogDescription>
          </DialogHeader>

          <div className="space-y-4 py-4">
            {/* Resumo da conta */}
            <div className="rounded-lg border bg-muted/50 p-4 space-y-2">
              <div className="flex justify-between items-start">
                <div>
                  <p className="font-medium text-sm">{conta.descricao}</p>
                  {conta.fornecedor && (
                    <p className="text-xs text-muted-foreground">
                      {conta.fornecedor.nomeFantasia || conta.fornecedor.razaoSocial}
                    </p>
                  )}
                </div>
                <div className="text-right">
                  <p className="font-bold text-lg">{formatarValor(conta.valor)}</p>
                  {conta.dataVencimento && (
                    <p className={cn('text-xs', isVencida ? 'text-destructive' : 'text-muted-foreground')}>
                      Venc: {format(new Date(conta.dataVencimento), 'dd/MM/yyyy')}
                    </p>
                  )}
                </div>
              </div>
            </div>

            {isVencida && (
              <Alert variant="destructive">
                <AlertCircle className="h-4 w-4" />
                <AlertDescription>
                  Esta conta está vencida. O pagamento será registrado com a data selecionada abaixo.
                </AlertDescription>
              </Alert>
            )}

            <Separator />

            {/* Forma de Pagamento */}
            <div className="space-y-2">
              <Label htmlFor="formaPagamento">
                Forma de Pagamento <span className="text-destructive">*</span>
              </Label>
              <Select
                value={formaPagamento}
                onValueChange={(value) => setFormaPagamento(value as FormaPagamentoContaPagar)}
              >
                <SelectTrigger id="formaPagamento">
                  <SelectValue placeholder="Selecione a forma de pagamento" />
                </SelectTrigger>
                <SelectContent>
                  {FORMAS_PAGAMENTO.map((forma) => (
                    <SelectItem key={forma.value} value={forma.value}>
                      {forma.label}
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>

            {/* Conta Bancária */}
            <div className="space-y-2">
              <Label htmlFor="contaBancaria">
                Conta Bancária <span className="text-destructive">*</span>
              </Label>
              <Select value={contaBancariaId} onValueChange={setContaBancariaId}>
                <SelectTrigger id="contaBancaria">
                  <SelectValue placeholder="Selecione a conta bancária" />
                </SelectTrigger>
                <SelectContent>
                  {contasBancarias.map((cb) => (
                    <SelectItem key={cb.id} value={cb.id.toString()}>
                      <div className="flex items-center gap-2">
                        <Building2 className="h-4 w-4 text-muted-foreground" />
                        {cb.nome}
                        {cb.banco && (
                          <span className="text-xs text-muted-foreground">({cb.banco})</span>
                        )}
                      </div>
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>

            {/* Data de Efetivação */}
            <div className="space-y-2">
              <Label>Data de Efetivação</Label>
              <Popover>
                <PopoverTrigger asChild>
                  <Button
                    variant="outline"
                    className={cn(
                      'w-full justify-start text-left font-normal',
                      !dataEfetivacao && 'text-muted-foreground'
                    )}
                  >
                    <CalendarIcon className="mr-2 h-4 w-4" />
                    {dataEfetivacao
                      ? format(dataEfetivacao, 'dd/MM/yyyy', { locale: ptBR })
                      : 'Selecione a data'}
                  </Button>
                </PopoverTrigger>
                <PopoverContent className="w-auto p-0" align="start">
                  <Calendar
                    mode="single"
                    selected={dataEfetivacao}
                    onSelect={setDataEfetivacao}
                    disabled={(date) => date > new Date()}
                    initialFocus
                    locale={ptBR}
                  />
                </PopoverContent>
              </Popover>
            </div>

            {/* Observações */}
            <div className="space-y-2">
              <Label htmlFor="observacoes">Observações</Label>
              <Textarea
                id="observacoes"
                placeholder="Observações sobre o pagamento..."
                value={observacoes}
                onChange={(e) => setObservacoes(e.target.value)}
                rows={2}
              />
            </div>

            {/* Comprovante de Pagamento */}
            <div className="space-y-2">
              <Label>Comprovante de Pagamento</Label>

              {comprovanteFile ? (
                <div className="flex items-center justify-between rounded-md border bg-muted/50 p-3">
                  <div className="flex items-center gap-2 overflow-hidden">
                    <FileText className="h-4 w-4 shrink-0 text-muted-foreground" />
                    <div className="overflow-hidden">
                      <p className="truncate text-sm font-medium">{comprovanteFile.name}</p>
                      <p className="text-xs text-muted-foreground">{formatFileSize(comprovanteFile.size)}</p>
                    </div>
                  </div>
                  <Button
                    type="button"
                    variant="ghost"
                    size="icon"
                    className="h-8 w-8 shrink-0"
                    onClick={handleRemoveFile}
                    disabled={comprovanteUploading}
                  >
                    <X className="h-4 w-4" />
                    <span className="sr-only">Remover arquivo</span>
                  </Button>
                </div>
              ) : (
                <div className="relative">
                  <input
                    type="file"
                    id="comprovante"
                    accept={COMPROVANTE_ALLOWED_TYPES.join(',')}
                    onChange={handleFileSelect}
                    className="absolute inset-0 cursor-pointer opacity-0"
                    disabled={comprovanteUploading}
                  />
                  <div className="flex cursor-pointer items-center justify-center gap-2 rounded-md border border-dashed p-4 text-sm text-muted-foreground transition-colors hover:border-primary hover:text-primary">
                    {comprovanteUploading ? (
                      <>
                        <Loader2 className="h-4 w-4 animate-spin" />
                        <span>Enviando...</span>
                      </>
                    ) : (
                      <>
                        <Upload className="h-4 w-4" />
                        <span>Clique para anexar comprovante</span>
                      </>
                    )}
                  </div>
                </div>
              )}

              {comprovanteError && (
                <p className="text-xs text-destructive">{comprovanteError}</p>
              )}

              <p className="text-xs text-muted-foreground">
                Formatos aceitos: PDF, JPG, PNG, WEBP. Máximo: 10MB.
              </p>
            </div>
          </div>

          <DialogFooter>
            <Button
              type="button"
              variant="outline"
              onClick={() => onOpenChange(false)}
              disabled={isSubmitting}
            >
              Cancelar
            </Button>
            <Button
              type="submit"
              disabled={isSubmitting || !formaPagamento || !contaBancariaId}
            >
              {isSubmitting ? 'Processando...' : 'Confirmar Pagamento'}
            </Button>
          </DialogFooter>
        </form>
      </DialogContent>
    </Dialog>
  );
}
