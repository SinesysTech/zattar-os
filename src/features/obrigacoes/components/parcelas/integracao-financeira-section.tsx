
'use client';

import * as React from 'react';
import { Button } from '@/components/ui/button';
import { Badge } from '@/components/ui/badge';
import { Skeleton } from '@/components/ui/skeleton';
import {
  AlertDialog,
  AlertDialogAction,
  AlertDialogCancel,
  AlertDialogContent,
  AlertDialogDescription,
  AlertDialogFooter,
  AlertDialogHeader,
  AlertDialogTitle,
} from '@/components/ui/alert-dialog';
import { RefreshCw, CheckCircle, AlertCircle, Clock, ExternalLink, ShieldCheck, Loader2 } from 'lucide-react';
import { toast } from 'sonner';
import Link from 'next/link';
import { cn } from '@/lib/utils';
// Keeping use-obrigacoes hook as is for now as it handles complex logic.
// If the hook is moving or deprecated, we would need to migrate its login into actions.
import { sincronizarAcordo, verificarConsistenciaAcordo } from '@/app/_lib/hooks/use-obrigacoes';

interface IntegracaoFinanceiraSectionProps {
  acordoId: number;
  onSyncComplete?: () => void;
}

export function IntegracaoFinanceiraSection({ acordoId, onSyncComplete }: IntegracaoFinanceiraSectionProps) {
  // Logic remains largely the same, just relocated. 
  // Any internal paths or types should work if use-obrigacoes is available.
  const [isLoading, setIsLoading] = React.useState(true);
  const [isSyncing, setIsSyncing] = React.useState(false);
  const [isVerifying, setIsVerifying] = React.useState(false);
  const [syncDialogOpen, setSyncDialogOpen] = React.useState(false);
  const [statusSync, setStatusSync] = React.useState<any>(null); // Type 'StatusSincronizacao' used to be local or imported
  const [inconsistencias, setInconsistencias] = React.useState<any[]>([]);

  const loadSyncStatus = React.useCallback(async () => {
    try {
      setIsLoading(true);
      const result = await verificarConsistenciaAcordo(acordoId);
      if (result.success && result.data) {
        const data = result.data;
        setInconsistencias(data.inconsistencias);
        setStatusSync({
          totalParcelas: data.totalParcelas,
          parcelasSincronizadas: data.parcelasSincronizadas,
          parcelasPendentes: data.parcelasPendentes,
          parcelasInconsistentes: data.parcelasInconsistentes,
        });
      }
    } catch (error) {
      console.error(error);
    } finally {
      setIsLoading(false);
    }
  }, [acordoId]);

  React.useEffect(() => { loadSyncStatus(); }, [loadSyncStatus]);

  const handleSincronizar = async (forcar: boolean = false) => {
    try {
      setIsSyncing(true);
      setSyncDialogOpen(false);
      const result = await sincronizarAcordo(acordoId, forcar);
      if (result.success && result.data) {
        toast.success(`Sincronização concluída: ${result.data.totalSucessos} processados.`);
        await loadSyncStatus();
        if (onSyncComplete) onSyncComplete();
      } else {
        toast.error(result.error || 'Erro ao sincronizar');
      }
    } catch (error) {
      toast.error('Erro ao sincronizar');
    } finally {
      setIsSyncing(false);
    }
  };

  const handleVerificarConsistencia = async () => {
    try {
      setIsVerifying(true);
      await loadSyncStatus();
      toast.success('Verificação concluída');
    } catch {
      toast.error('Erro ao verificar');
      setIsVerifying(false);
    }
  };

  if (isLoading) return <Skeleton className="h-40 w-full" />;

  const temInconsistencias = inconsistencias.length > 0;
  const statusGeral = !temInconsistencias ? 'sincronizado' : inconsistencias.some(i => i.tipo === 'parcela_sem_lancamento') ? 'pendente' : 'inconsistente';

  return (
    <div className="rounded-lg border bg-card p-6">
       <div className="flex items-center justify-between mb-4">
         <h2 className="text-lg font-semibold flex items-center gap-2">
           <RefreshCw className="h-5 w-5" /> Integração Financeira
         </h2>
         <Badge variant="outline" className={cn('gap-1', 
            statusGeral === 'sincronizado' && 'text-green-600 border-green-600',
            statusGeral === 'pendente' && 'text-amber-600 border-amber-600',
            statusGeral === 'inconsistente' && 'text-red-600 border-red-600'
         )}>
            {statusGeral === 'sincronizado' ? <CheckCircle className="h-3 w-3" /> : 
             statusGeral === 'pendente' ? <Clock className="h-3 w-3" /> : <AlertCircle className="h-3 w-3" />}
            {statusGeral.charAt(0).toUpperCase() + statusGeral.slice(1)}
         </Badge>
       </div>

       {statusSync && (
          <div className="grid grid-cols-2 md:grid-cols-4 gap-4 mb-4">
             <div className="rounded-lg bg-muted/50 p-3">
                <p className="text-xs text-muted-foreground">Total</p>
                <p className="text-xl font-bold">{statusSync.totalParcelas}</p>
             </div>
             <div className="rounded-lg bg-green-50 p-3">
                <p className="text-xs text-muted-foreground">Sincronizadas</p>
                <p className="text-xl font-bold text-green-600">{statusSync.parcelasSincronizadas}</p>
             </div>
             <div className="rounded-lg bg-amber-50 p-3">
                <p className="text-xs text-muted-foreground">Pendentes</p>
                <p className="text-xl font-bold text-amber-600">{statusSync.parcelasPendentes}</p>
             </div>
             <div className="rounded-lg bg-red-50 p-3">
                <p className="text-xs text-muted-foreground">Inconsistentes</p>
                <p className="text-xl font-bold text-red-600">{statusSync.parcelasInconsistentes}</p>
             </div>
          </div>
       )}

       <div className="flex flex-wrap gap-2">
          <Button variant="outline" size="sm" onClick={() => setSyncDialogOpen(true)} disabled={isSyncing || isVerifying}>
             {isSyncing ? <Loader2 className="h-4 w-4 mr-2 animate-spin" /> : <RefreshCw className="h-4 w-4 mr-2" />}
             Sincronizar
          </Button>
          <Button variant="outline" size="sm" onClick={handleVerificarConsistencia} disabled={isSyncing || isVerifying}>
             {isVerifying ? <Loader2 className="h-4 w-4 mr-2 animate-spin" /> : <ShieldCheck className="h-4 w-4 mr-2" />}
             Verificar
          </Button>
          <Button variant="outline" size="sm" asChild>
             <Link href={`/financeiro/obrigacoes?acordoId=${acordoId}`}>
                <ExternalLink className="h-4 w-4 mr-2" /> Ver no Financeiro
             </Link>
          </Button>
       </div>

       <AlertDialog open={syncDialogOpen} onOpenChange={setSyncDialogOpen}>
         <AlertDialogContent>
           <AlertDialogHeader>
             <AlertDialogTitle>Sincronizar</AlertDialogTitle>
             <AlertDialogDescription>Esta ação irá atualizar os lançamentos financeiros.</AlertDialogDescription>
           </AlertDialogHeader>
           <AlertDialogFooter>
             <AlertDialogCancel>Cancelar</AlertDialogCancel>
             <AlertDialogAction onClick={() => handleSincronizar(false)}>Sincronizar</AlertDialogAction>
             {temInconsistencias && <AlertDialogAction onClick={() => handleSincronizar(true)} className="bg-amber-600">Forçar</AlertDialogAction>}
           </AlertDialogFooter>
         </AlertDialogContent>
       </AlertDialog>
    </div>
  );
}
